% ==========================================================
% 脚本功能：生成 1张 纯白/无湍流 理想相位屏 (0相位畸变)
% ==========================================================
clear all;
clc;

% --- 基础参数设置 (严格对齐硬件，保持不变) ---
D = 10e-3;      % 物理边长 (10 mm)
N = 1920;       % 每一边的网格点数 (SLM 宽度分辨率)
L0 = 10;        % 外尺度 (固定参数)
l0 = 0.1e-3;    % 内尺度 (固定参数)
delta = D/N;    % 网格物理间距

% 确保保存路径存在
save_dir = 'D:\screen\';
if ~exist(save_dir, 'dir')
    mkdir(save_dir);
end

% ==================== 核心修改：纯白无湍流 ====================
% 1. 生成全0相位矩阵 = 无任何湍流畸变
phz = zeros(N, N);  

% 2. SLM 硬件适配处理
dis = mod(phz, 2*pi);    % 相位限制 0~2pi
bb = mat2gray(dis);      % 全0 → 映射为纯白灰度图 (值=1)

% 3. 裁剪至 1080×1920 并保存
filename = [save_dir, 'Flat_Ideal.png'];  % 命名：理想纯白相位屏
imwrite(bb(1:1080, :), filename, 'png');

fprintf('✅ 纯白无湍流相位屏已生成：%s\n', filename);